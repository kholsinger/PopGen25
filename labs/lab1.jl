# ‚ïî‚ïê‚ï° 3e099118-c0c7-4cf5-9a6f-86ddc971883c
function WrightFisher_long(N,p0)
	current_freq = p0
	g = 1
	while current_freq*(1-current_freq) != 0#
		next_freq = rand(Binomial(N,current_freq),1)[1]/N
		g += 1
		current_freq = next_freq
	end
	return(current_freq, g)
end

